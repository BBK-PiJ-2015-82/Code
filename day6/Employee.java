public class Employee {

	//private fields

	private String name;
	private int number;

	//public methods

	public void setName(String aName) {
		this.name = aName;
	}
	public String getName() {
		return this.name;
	}

	public void setNumber(String aNumber) {
		this.number = aNumber;
	}
	public String getNumber() {
		return this.number;
	}


}	